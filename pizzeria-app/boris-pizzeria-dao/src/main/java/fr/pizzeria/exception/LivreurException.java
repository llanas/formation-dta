package fr.pizzeria.exception;

public class LivreurException extends Exception {

	public LivreurException() {
		super();
	}

	public LivreurException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LivreurException(String arg0) {
		super(arg0);
	}

	public LivreurException(Throwable arg0) {
		super(arg0);
	}
	
	

}
